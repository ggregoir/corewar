#include "libmat.h"

t_vec3	norme(float x, float y, float z)
{
	t_vec3	u;
	float	tmp;

	tmp = sqrt(1 / (x * x + y * y + z * z));
	u.x = x * tmp;
	u.y = y * tmp;
	u.z = z * tmp;
	return (u);
}
