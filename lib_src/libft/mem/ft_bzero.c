#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	while (n > 8)
	{
		*(t_ulint *)s = 0;
		s += 8;
		n -= 8;
	}
	while (n > 0)
	{
		*(t_uchar *)s = 0;
		s++;
		n--;
	}
}
