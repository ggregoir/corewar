#include "libmat.h"
#include <stdio.h>

void	mat4_put(t_mat4 m)
{
	printf("\n%5.1f%5.1f%5.1f%5.1f\n", m[0], m[4], m[8], m[12]);
	printf("%5.1f%5.1f%5.1f%5.1f\n", m[1], m[5], m[9], m[13]);
	printf("%5.1f%5.1f%5.1f%5.1f\n", m[2], m[6], m[10], m[14]);
	printf("%5.1f%5.1f%5.1f%5.1f\n\n", m[3], m[7], m[11], m[15]);
}
