#include "libmat.h"

t_vec4	mat4_vec4_mult(t_mat4 m, t_vec4 u)
{
	t_vec4	v;

	v.x = m[0] * u.x + m[4] * u.y + m[8] * u.z + m[12] * u.w;
	v.y = m[1] * u.x + m[5] * u.y + m[9] * u.z + m[13] * u.w;
	v.z = m[2] * u.x + m[6] * u.y + m[10] * u.z + m[14] * u.w;
	v.w = m[3] * u.x + m[7] * u.y + m[11] * u.z + m[15] * u.w;
	return (v);
}
