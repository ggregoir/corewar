#include "asm.h"

int		arglen(char *str)
{
	int		i;

	i = 0;
	while (str[i] && str[i] != COMMENT_CHAR)
		i++;
	while (str[i - 1] == ' ' || str[i - 1] == '\t')
		i--;
	return (i);
}
