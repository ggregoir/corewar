#include "asm.h"

int		labelcmp(char *label1, char *label2)
{
	while (*label1 == *label2 && *label1)
	{
		label1++;
		label2++;
	}
	return (*label1 == 0 || *label1 == ' ' || *label1 == '\t' ||
			*label1 == '+' || *label1 == '-');
}
