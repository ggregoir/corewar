#include "vm.h"

t_uint16    get_uint16(t_uint32 pc)
{
    t_uint32    ret;

    ret = g_mem[(t_uint32)(pc + 0) % MEM_SIZE];
    ret = (ret << 8) + g_mem[(t_uint32)(pc + 1) % MEM_SIZE];
    return (ret);
}
