#include "vm.h"

t_uint8	check_reg(t_inst *args)
{
	t_int32	i;

	i = 0;
	while (i < 4)
	{
		if (args[i].type == T_REG)
		{
			if (args[i].value == -1)
				return (0);
		}
		i++;
	}
	return (1);
}
