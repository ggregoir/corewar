#include "vm.h"

void		op_add(t_proc *p, t_inst *args)
{
	p->reg[args[2].value - 1] = p->reg[args[0].value - 1]
		+ p->reg[args[1].value - 1];
	p->carry = !p->reg[args[2].value - 1];
}
