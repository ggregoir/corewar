#include "vm.h"

void 		op_ld(t_proc *p, t_inst *args)
{
    t_int32 tmp;

    tmp = get_real_value(args, p);
    p->reg[args[1].value - 1] = tmp;
    if (!tmp)
        p->carry = 1;
}