#include "vm.h"

void 		op_zjmp(t_proc *p, t_inst *args)
{
    if (p->carry == 1)
    {
        p->pc += args[0].value % IDX_MOD;
        g_color[p->pc % MEM_SIZE] = (t_case){0.0f, 1.0f, 0.0f};
        p->pc -= 3;
    }
}