#include "vm.h"

void	get_type(t_uchar mem, t_uchar *type)
{
	type[3] = mem & 0b11;
	type[2] = (mem & (0b11 << 2)) >> 2;
	type[1] = (mem & (0b11 << 4)) >> 4;
	type[0] = (mem & (0b11 << 6)) >> 6;
}
