#include "display.h"

void    put_chat(void)
{
    int     i;
    int     y;

    y = 0;
    i = g_line_chat;
    while (i >= 0)
    {
        put_text(g_chat[i], 0.43f, -0.95f + (float)(y) * 0.08f);
        i--;
        y++;
    }
    i = 9;
    while (i > g_line_chat)
    {
        put_text(g_chat[i], 0.43f, -0.95f + (float)(y) * 0.08f);
        i--;
        y++;
    }
}